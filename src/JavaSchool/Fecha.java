package JavaSchool;

public class Fecha {

    private String fecha;

    public Fecha(String fecha) {
        this.fecha = fecha;
    }

    public Fecha() {
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String mostrarFecha() {
        return fecha;

    }

}
