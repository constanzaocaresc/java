package JavaSchool;

public class Carrera {

    private String carrera;

    public Carrera(String carrera) {
        this.carrera = carrera;
    }

    public Carrera() {
    }

    public String getCarrera() {
        return carrera;
    }

    public void setCarrera(String carrera) {
        this.carrera = carrera;
    }

    public String mostrarCarrera() {
        return carrera;
    }

}
